function potvrdi()
{
    if(odabrani_brojevi.length == 7)
    {
        document.getElementById("p1").innerHTML = odabrani_brojevi;
        document.getElementById("izvuci").disabled = false;
    }
    else
        alert("Molimo, odaberite 7 brojeva.");
}

function izvuci()
{
    var br = 0;
    for(var i = 1; i < Number.POSITIVE_INFINITY; i++)
    {
        var x = Math.floor(Math.random() * 39) + 1;
        if(!izvuceni_brojevi.includes(x) && br < 7)
        {
            izvuceni_brojevi.push(x);
            br++;
        } 
        else if(br == 7)
        {
            document.getElementById("p2").innerHTML = izvuceni_brojevi;
            document.getElementById("izvuci").disabled = true;
            document.getElementById("potvrdi").disabled = true;
            document.getElementById("dobitak").disabled = false;
            break;
        }
    }
}

function dobitak()    
{
    var td = document.getElementsByTagName("td");
    var br = 0;
    for(var i = 0; i < odabrani_brojevi.length; i++)
        if(izvuceni_brojevi.includes(odabrani_brojevi[i]))
            br++;
    document.getElementById("p3").style.display = "block";
    document.getElementById("s1").innerHTML += br;
    document.getElementById("dobitak").disabled = true;
    for(var i = 0; i < odabrani_brojevi.length; i++)
        for(var j = 0; j < izvuceni_brojevi.length; j++)
            if(odabrani_brojevi[i] == izvuceni_brojevi[j])
            {
                document.getElementById("p4").style.display = "block";
                document.getElementById("s2").innerHTML += "<br>" + odabrani_brojevi[i];
                for(var k = 0; k < td.length; k++)
                    td[odabrani_brojevi[i] - 1].style.backgroundColor = "#99ffbb";
            }     
}
